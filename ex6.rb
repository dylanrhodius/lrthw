# variable stating that there are "10" types of people
types_of_people = 10

# string variable interpolating value of above variable
x = "There are #{types_of_people} types of people."

# string variables
binary = "binary"
do_not = "don't"

# string variable interpolating values of above variables
y = "Those who know #{binary} and those who #{do_not}."

# puts value of variables
puts x
puts y

# puts value of variables
puts "I said: #{x}."
puts "I also said: '#{y}'."

